import cv2
import numpy as np

original=cv2.imread(r"C:\Users\Welcome\Downloads\daisy_flower.jpg")
bgrl=np.array([90,0,120],np.uint8)
bgrh=np.array([225,255,255],np.uint8)

mask=cv2.inRange(original,bgrl,bgrh)
kernal=np.array(([2,4,2],[3,1,2],[4,3,2]),np.uint8)
blured=cv2.filter2D(mask,-1,kernal)
avg=cv2.blur(mask,(5,5))
gaus=cv2.GaussianBlur(mask,(5,5),0)
median=cv2.medianBlur(mask,7)
bilateral=cv2.bilateralFilter(mask,5,111,111)
cv2.imshow("original",original)
cv2.imshow("blured",blured)
cv2.imshow("mask",mask)
cv2.imshow("average blur",avg)
cv2.imshow("GaussianBlur",gaus)
cv2.imshow("median blur",median)
cv2.imshow("bilateralFilter",bilateral)
resize=cv2.resize(original,(500,650))
print(resize.shape)
img = cv2.rectangle(resize,(200,600),(400,550),(0,255,0),-1)
img=cv2.ellipse(img,(300,600),(100,60),0,0,180,(0,255,0),-1)
cv2.putText(img,"Daisy",(150,70),cv2.FONT_ITALIC,3,(0,215,255),3)
cv2.imshow("img",img)
cv2.waitKey(0)
cv2.destroyAllWindows()